HEADERS = ["날짜", "채널", "방송시간", "상품명", "건강카테고리", "가격(원)", "키워드"]


def write_csv() -> str:
    with open(OUTPUT_PATH, "w", newline="", encoding="utf-8-sig") as f:
        writer = csv.writer(f)
        writer.writerow(HEADERS)
        for row in HEALTH_ITEMS:
            writer.writerow(row)
    return OUTPUT_PATH


def print_keyword_summary() -> None:
    from collections import Counter
    categories = Counter()
    keywords: dict[str, int] = Counter()
    channels = Counter()

    for item in HEALTH_ITEMS:
        categories[item[4]] += 1
        channels[item[1]] += 1
        for kw in item[6].split(", "):
            keywords[kw.strip()] += 1

    print("\n=== 건강식품 카테고리별 분류 ===")
    for cat, cnt in categories.most_common():
        print(f"  {cat}: {cnt}건")

    print("\n=== 채널별 건강식품 수 ===")
    for ch, cnt in channels.most_common():
        print(f"  {ch}: {cnt}건")

    print("\n=== 상위 키워드 (빈도순) ===")
    for kw, cnt in keywords.most_common(20):
        print(f"  {kw}: {cnt}회")


