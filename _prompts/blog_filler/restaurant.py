# 리스트형 키워드 감지 패턴
_LIST_PATTERNS = re.compile(
    r"(\d+선|\d+곳|리스트|추천\s*리스트)"
)


def is_list_keyword(keyword: str) -> bool:
    """키워드가 리스트형인지 판별"""
    return bool(_LIST_PATTERNS.search(keyword))


