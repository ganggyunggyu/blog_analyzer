def get_blog_filler_restaurant_user_prompt(
    keyword: str,
    area_name: str = "",
    pen_name: str = "",
    restaurants_text: str = "",
    count: int = 7,
) -> str:
    """맛집 리스트 전용 유저 프롬프트"""
    parts = [
        f"키워드: {keyword}",
        f"지역: {area_name}",
        f"맛집 수: {count}곳",
    ]

    if restaurants_text:
        parts.append(f"\n[맛집 데이터]\n{restaurants_text}")
        parts.append("\n위 데이터 기반으로 <reference> 원고와 똑같은 톤/말투/리듬으로 블로그 원고를 써줘.")
        parts.append("가게명과 대표메뉴는 데이터 그대로 쓰고, 나머지는 자연스럽게 풀어서.")
    else:
        parts.append(f"\n{area_name} 지역 맛집을 <reference> 원고 톤으로 써줘.")

    parts.append("\n제목과 본문만 출력.")

    return "\n".join(parts)
