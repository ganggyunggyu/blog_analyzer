def _normalize_keyword(keyword: str) -> str:
    return keyword.strip() if keyword.strip() else "[메인 키워드 미입력]"


def _get_search_focus(keyword: str) -> tuple[str, str]:
    compact_keyword = keyword.replace(" ", "").lower()

    if any(token in compact_keyword for token in ("가격", "비용", "수술비", "렌즈값", "얼마", "보험", "실손")):
        return (
            "비용/실용 중심형",
            "비용 구성, 가격대별 차이, 건강보험/실손보험 적용 여부, 절약 팁, 주의사항을 본문 중심으로 풀어쓸 것",
        )

    if any(token in compact_keyword for token in ("부작용", "후유증", "통증", "위험", "후회", "재수술", "빛번짐")):
        return (
            "문제해결형",
            "흔한 고민, 원인 분석, 해결방법, 예방법, 수술 전 준비사항을 구체적으로 설명할 것",
        )

    if any(token in compact_keyword for token in ("회복", "회복기간", "관리", "주의사항", "주의", "과정", "전후")):
        return (
            "단계별 가이드형",
            "수술 전 준비, 수술 당일, 수술 직후, 회복기, 장기관리처럼 시간순으로 경과와 생활 기준을 설명할 것",
        )

    if any(token in compact_keyword for token in ("비교", "차이", "vs", "라식라섹", "단초점", "다초점")):
        return (
            "비교형",
            "비교 대상의 개념, 통증, 회복 속도, 안정성, 적합 대상, 비용 차이를 서술형으로 비교할 것",
        )

    if any(token in compact_keyword for token in ("체크", "자가진단", "선택", "기준", "가능여부")):
        return (
            "체크리스트형",
            "자가진단, 조건별 체크, 결과 해석, 추천 선택지, 병원 선택 팁을 의사결정 흐름으로 설명할 것",
        )

    return (
        "정보 제공형",
        "정의와 원리, 적합 대상, 장점, 단점과 부작용, 비용, 회복 기간, 사후관리, 병원 선택 기준을 균형 있게 배치할 것",
    )


