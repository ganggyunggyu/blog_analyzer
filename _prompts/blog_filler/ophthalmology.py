"""Blog Filler 안과 카테고리 전용 시스템 프롬프트."""

from __future__ import annotations

from _prompts.rules.line_break_rules import get_line_break_rules


def _normalize_keyword(keyword: str) -> str:
    return keyword.strip() if keyword.strip() else "[메인 키워드 미입력]"


def _get_search_focus(keyword: str) -> tuple[str, str]:
    compact_keyword = keyword.replace(" ", "").lower()

    if any(token in compact_keyword for token in ("가격", "비용", "수술비", "렌즈값", "얼마")):
        return (
            "가격/비용 중심형",
            "비용 구성, 급여/비급여, 보험 적용 여부, 렌즈 종류, 장비, 검사 범위, 사후관리 차이를 본문 중심으로 풀어쓸 것",
        )

    if any(token in compact_keyword for token in ("부작용", "후유증", "통증", "위험", "재수술")):
        return (
            "부작용 점검형",
            "흔한 불편, 발생 원인, 예방 기준, 바로 병원 확인이 필요한 위험 신호를 구분해서 설명할 것",
        )

    if any(token in compact_keyword for token in ("회복", "회복기간", "관리", "주의사항", "주의", "과정")):
        return (
            "회복/과정 가이드형",
            "수술 전 검사, 수술 당일, 3일, 1주, 1개월, 3개월 흐름처럼 시기별 경과와 생활 기준을 구체적으로 설명할 것",
        )

    if any(token in compact_keyword for token in ("비교", "차이", "vs", "라식라섹")):
        return (
            "비교 분석형",
            "라식, 라섹, 스마일라식, 렌즈삽입술 등 비교 대상의 구조, 회복, 통증, 비용, 적합 대상을 서술형으로 비교할 것",
        )

    return (
        "종합 정보형",
        "개념, 원리, 적합 대상, 과정, 회복, 비용, 주의사항을 균형 있게 배치할 것",
    )


def _get_required_sections(keyword: str) -> str:
    compact_keyword = keyword.replace(" ", "")

    if "백내장" in compact_keyword:
        return """
B. 백내장 키워드 우선 섹션
1. 백내장이란 어떤 질환인지
2. 수술이 필요한 시점과 증상
3. 백내장수술 과정 단계별 설명
4. 인공수정체 단초점, 다초점 종류와 차이
5. 비용 구성, 급여/비급여, 실손보험 적용 여부
6. 시기별 회복 과정과 주의사항
7. 즉시 병원에 방문해야 하는 위험 신호
""".strip()

    return """
A. 시력교정술 키워드 우선 섹션
1. 해당 시술의 개념과 원리
2. 라식, 라섹, 스마일라식, 렌즈삽입술 등 다른 시술과의 차이
3. 적합 대상과 부적합 가능성이 있는 대상
4. 수술 과정 단계별 설명
5. 회복기간과 시기별 경과
6. 주의사항 및 부작용
7. 비용/가격 구성 요소
""".strip()


