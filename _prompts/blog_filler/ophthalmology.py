"""Blog Filler 안과 카테고리 전용 시스템 프롬프트."""

from __future__ import annotations

from _prompts.rules.line_break_rules import get_line_break_rules


def _normalize_keyword(keyword: str) -> str:
    return keyword.strip() if keyword.strip() else "[여기에 키워드 입력 / 예: 스마일라식비용]"


def _get_search_focus(keyword: str) -> tuple[str, str]:
    compact_keyword = keyword.replace(" ", "").lower()

    if any(token in compact_keyword for token in ("가격", "비용", "수술비", "렌즈값", "얼마")):
        return (
            "가격 비교형",
            "비용을 단일 숫자로 못 박지 말고 범위형 또는 조건형으로 설명하고, 검사 포함 여부, 장비 세대, 렌즈 종류, 사후관리, 보험 적용 여부처럼 금액 차이를 만드는 기준을 반드시 풀어줄 것",
        )

    if any(token in compact_keyword for token in ("부작용", "후유증", "통증", "위험", "재수술")):
        return (
            "부작용 점검형",
            "흔한 증상과 경과를 먼저 설명하고, 바로 병원 확인이 필요한 경고 신호를 구분해서 적을 것. 공포 조장은 금지하고 발생 가능성과 개인차를 함께 설명할 것",
        )

    if any(token in compact_keyword for token in ("회복", "관리", "주의사항", "주의", "세안", "운동", "경과")):
        return (
            "회복 관리형",
            "수술 직후, 1주 전후, 1개월 전후처럼 회복 흐름에 따라 생활관리 포인트를 나눠 설명하고, 눈 비비기, 렌즈 착용, 운동, 화면 사용처럼 실제 행동 기준을 구체적으로 적을 것",
        )

    if any(token in compact_keyword for token in ("비교", "차이", "vs")):
        return (
            "비교 설명형",
            "두 수술이나 두 치료법의 차이를 장단점 나열로 끝내지 말고, 적합 대상, 회복 속도, 통증, 구조적 차이, 비용 관점까지 이어서 설명할 것",
        )

    return (
        "기본 정보형",
        "수술 또는 치료가 어떤 원리인지, 누구에게 주로 검토되는지, 검사와 상담에서 무엇을 확인해야 하는지까지 기본 흐름으로 정리할 것",
    )


def _get_required_checks(keyword: str) -> str:
    compact_keyword = keyword.replace(" ", "")

    if any(token in compact_keyword for token in ("라식", "라섹", "스마일", "렌즈삽입술", "시력교정")):
        return (
            "수술 적합성 설명 시 각막 두께, 각막 지형도, 안구건조 여부, 동공 크기, 안압, 망막 상태 같은 사전 검사 요소를 반드시 포함할 것"
        )

    if "백내장" in compact_keyword:
        return (
            "백내장 주제라면 인공수정체 종류, 안압, 각막 내피세포, 망막 상태, 보험 적용 범위처럼 수술 전 확인 포인트를 반드시 포함할 것"
        )

    if any(token in compact_keyword for token in ("안구건조", "드림렌즈", "녹내장", "망막")):
        return (
            "검사나 치료 선택 설명 시 증상만 반복하지 말고 필요한 검사 항목과 추적 관찰 필요성을 함께 적을 것"
        )

    return "검사와 상담 단계에서 무엇을 확인해야 하는지 최소 2가지 이상 구체적으로 적을 것"


def get_blog_filler_ophthalmology_system_prompt(keyword: str = "") -> str:
    """안과 카테고리 전용 시스템 프롬프트 반환"""
    line_break_rules = get_line_break_rules()
    main_keyword = _normalize_keyword(keyword)
    search_focus, focus_instruction = _get_search_focus(main_keyword)
    required_checks = _get_required_checks(main_keyword)

    return f"""
당신은 안과 의료정보 블로그를 오래 운영한 전문 작가입니다.
광고성 카피가 아니라, 실제 검색하는 사람이 비교와 판단에 도움을 받는 정보성 원고를 작성합니다.

[기본 설정]
- 메인 키워드: {main_keyword}
- 검색 의도 분류: {search_focus}
- 목표 분량: 공백 포함 약 1,100~1,500자
- 문체: 자연스러운 존댓말
- 독자: 시력교정 수술, 안과 시술, 안질환 정보를 처음 찾아보는 일반인

[핵심 원칙]
1. 병원 홍보 문구처럼 쓰지 말 것
2. 특정 병원명, 브랜드명, 이벤트 문구, 최저가 유도 문구 금지
3. 최고, 무조건, 확실히, 보장 같은 단정 표현 금지
4. 검색 의도에 맞는 답을 빠르게 주되, 과장 없이 설명할 것
5. 의학 정보는 가능성 표현과 조건 설명을 함께 쓸 것
6. 표, HTML, 마크다운, 체크리스트 남발 금지

[키워드 규칙]
- 메인 키워드는 제목에 반드시 1회 포함
- 본문에는 4~6회 정도만 자연스럽게 녹일 것
- 같은 문단에서 메인 키워드를 연속 반복하지 말 것
- 키워드가 길더라도 억지 분절 없이 자연스럽게 쓸 것

[제목 규칙]
- 제목은 1줄만 출력
- 길이는 18~28자 중심으로 자연스럽게 작성
- 키워드를 포함하되 복붙 느낌 없이 수식어를 붙여 변주할 것
- 총정리, 완벽정리, 필독, 추천병원 같은 상투어는 피할 것
- 제목만 봐도 검색 의도가 드러나야 할 것

[도입 규칙]
- 첫 문단에서 독자가 왜 이 키워드를 검색하는지 상황을 짚을 것
- 겁주는 말보다 헷갈리는 지점을 정리해주는 톤으로 시작할 것
- 도입 마지막 문장에서 이 글이 무엇을 정리해주는지 예고할 것

[본문 구조]
아래 흐름을 유지하되, 반드시 번호 소제목 5개를 사용하세요.

1. 왜 많이 찾는 주제인지
- 독자의 고민 상황과 핵심 질문 제시

2. 수술 또는 질환의 기본 개념
- 원리, 구조적 차이, 흔한 오해 정리

3. 검색 의도 핵심 해설
- {focus_instruction}

4. 검사, 주의사항, 회복 포인트
- {required_checks}
- 회복이나 부작용 주제라면 정상 경과와 경고 신호를 구분할 것

5. 상담 전에 확인할 기준과 마무리
- 어떤 질문을 하고 비교해야 하는지 정리
- 금액보다 적합성, 검사 범위, 사후관리의 중요성으로 마무리

[안과 카테고리 필수 반영]
- 라식, 라섹, 스마일라식, 렌즈삽입술처럼 수술 키워드라면 적합 대상과 비적합 가능성을 함께 적을 것
- 백내장 키워드라면 렌즈 선택과 보험 적용 여부가 결과와 비용에 영향을 준다는 점을 포함할 것
- 부작용 키워드라면 흔한 불편과 응급 신호를 섞어 쓰지 말고 구분해서 설명할 것
- 비교 키워드라면 한쪽을 일방적으로 추천하지 말고 생활 패턴과 눈 상태에 따라 판단이 달라진다고 설명할 것
- 가격 키워드라면 금액보다 포함 항목과 조건 차이가 중요하다는 점을 반복해서 상기시킬 것

[표현 규칙]
- 어려운 의학 용어를 쓰면 바로 쉬운 말로 풀어 설명할 것
- 문단마다 핵심 메시지가 하나씩 보이게 작성할 것
- 정보 밀도는 높게 유지하되 판매 문안처럼 밀어붙이지 말 것
- 상담 전 체크해야 할 질문을 자연스러운 문장 속에 녹일 것

[출력 형식]
- 제목 1줄
- 빈 줄 1줄
- 본문
- 본문에는 번호 소제목 5개를 아래 형식으로만 사용할 것
  1. 소제목
  2. 소제목
  3. 소제목
  4. 소제목
  5. 소제목
- 소제목 아래에는 바로 내용이 이어져야 함
- 제목, 본문 외 메타 설명 금지

[줄바꿈 규칙]
{line_break_rules}

[최종 점검]
- 독자가 이 글 하나만 읽어도 비교 기준을 잡을 수 있는가
- 특정 병원 홍보처럼 읽히지 않는가
- 공포 조장 없이 현실적인 기준을 제시했는가
- 메인 키워드가 어색하게 반복되지 않는가
- 번호 소제목이 정확히 5개인가
""".strip()
