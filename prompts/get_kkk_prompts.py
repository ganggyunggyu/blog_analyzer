class KkkPrompt:
    """
    테스트용 프롬프트 생성기
    - 임시 테스트 프롬프트 템플릿 제공
    """

    @staticmethod
    def test_prompt_v1(keyword: str) -> str:
        return f"""
테스트 프롬프트 v1입니다.

키워드: {keyword}

여기에 테스트용 프롬프트 내용을 추가하세요.
"""

    @staticmethod
    def test_prompt_v2(keyword: str, length: int = 1800) -> str:
        return f"""
테스트 프롬프트 v2입니다.

키워드: {keyword}
목표 길이: {length}자

여기에 테스트용 프롬프트 내용을 추가하세요.
"""

    @staticmethod
    def experimental_prompt(
        keyword: str, 
        style: str = "casual", 
        length: int = 2000,
        note: str = ""
    ) -> str:
        return f"""
실험용 프롬프트입니다.

키워드: {keyword}
스타일: {style}
목표 길이: {length}자
추가 요청사항: {note}

여기에 실험용 프롬프트 내용을 추가하세요.
"""

    @staticmethod
    def get_kkk_system_prompt_v2() -> str:
        return """
당신은 네이버 블로그 SEO 최적화 글쓰기 전문가입니다.
나는 특정 주제를 주면, 네이버 **상위노출 알고리즘(D.I.A 로직 + 원고지수 중심)**에 맞게
후기성 원고를 작성해야 합니다.

[특수문자 사용 지침]

1. 가운데 점(·) 사용 금지 → 반드시 쉼표(,)로 대체
   예시: 루틴, 성분, 복용

2. 작은따옴표(')와 큰따옴표(") 절대 사용 금지

3. 마침표(.) 절대 사용 금지 
   단, 부제(소제목)에서는 예외적으로 사용할 수 있음
   단, 부제(소제목)에서도 마지막에서는 사용할 수 없음

4. 마크다운 문법(#, *, -, ``` 등) 절대 사용 금지 
   네이버 블로그는 이를 지원하지 않으므로 가독성을 해침

5. 짧은 문장을 마구 끊지 말고  
   자연스럽게 이어진 문장으로 작성해야 함

[문장 구조 지침]

1. 한 줄은 40자를 넘기지 않도록 작성  
   줄바꿈 시 반드시 이음세(그래서, 그리고, 또한, 하지만 등)를 활용하여  
   문장이 매끄럽게 이어지도록 할 것

2. 짧은 문장을 마구 끊지 말고  
   자연스럽게 흐르는 리듬으로 작성해야 함
   2~3줄마다 줄바꿈
   한 문단은 3~5줄 유지

3. 문장의 끝맺음은 다양하게 사용할 것  
    '요', '다', '죠', '구요', '답니다' 등 여러 표현을 섞어  
    같은 어미가 2회 이상 반복되지 않도록 조정해야 함
"""