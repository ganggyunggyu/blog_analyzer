class KkkPrompt:
    """
    테스트용 프롬프트 생성기
    - 임시 테스트 프롬프트 템플릿 제공
    """

    @staticmethod
    def kkk_prompt_gpt_5(
        keyword: str | None = "",
        min_length: int | None = 2300,
        max_length: int | None = 2400,
        note: str | None = "",
    ) -> str:
        return f"""
---
- 키워드: ({keyword})

[지시사항]
- 키워드 기반의 블로그 원고 작성
- 글자 수 공백 제외 {min_length}~{max_length}자 사이를 필수로 지켜야합니다.
- 키워드 제외하고 다른 형태소 절대 4개 이상 넘어가면 안됩니다.
   - 다양한 표현 사용으로 반복적으로 겹치는 단어 및 문장 사용을 하지 않도록 합니다.

[추가 요청사항]

- 이 부분은 유저가 추가로 요청하는 부분으로 반드시 이행 되어야 합니다.

{note}

---
  
  [정보성 후기]
1. **목적**: 독자가 주제에 대해 배경 지식, 일반 정보, 팁을 습득하도록 돕는다.
2. **구성**: 서론(주제 개요) → 본문(관련 정보, 팁, 주의사항, 데이터 등) → 정리.
3. **내용 특징**:
   - 제품·서비스 사용 경험이 없어도 작성 가능.
   - 객관적인 사실, 데이터, 공인된 정보 포함.
   - 특정 브랜드·제품 홍보보다 **주제 자체의 정보 전달**이 목적.
   - 비교, 장단점, 사용 방법, 관련 팁 등을 포함.
4. **톤**:
   - 친절하고 설명적인 어조.
   - 감정 표현 최소화, 독자의 이해를 돕는 명확한 문장.
5. **예시 주제**:
   - “스마일라식 수술 절차와 주의사항”
   - “여름철 눈 건강 관리 방법”

---

[리뷰형 후기]
1. **목적**: 본인의 실제 경험을 바탕으로 제품·서비스의 장단점, 만족도, 감정을 전달한다.
2. **구성**: 인삿말 → 배경/사용 계기 → 사용 과정 → 결과/느낌 → 추천 여부.
3. **내용 특징**:
   - 실제 사용 경험 기반.
   - 주관적인 의견·느낌·개인적 상황 강조.
   - 구체적인 에피소드, 감정 표현, 개인 취향 반영.
   - 제품·서비스의 세부 기능, 장단점, 가격 등 포함.
4. **톤**:
   - 개인적인 서술(1인칭 시점)과 감정 표현 풍부.
   - “좋았다”, “아쉬웠다” 등 평가성 문구 포함.
5. **예시 주제**:
   - “내가 스마일라식을 선택한 이유와 수술 후 6개월 변화”
   - “한 달 사용한 OOO 선글라스 솔직 후기”

---
[구분 기준 요약]
- **경험 유무**: 정보성은 경험 없어도 작성 가능 / 리뷰형은 실제 경험 필수.
- **객관성**: 정보성은 객관적·설명 중심 / 리뷰형은 주관적·경험 중심.
- **목적**: 정보성은 지식·정보 전달 / 리뷰형은 경험 공유 및 평가.
- **톤**: 정보성은 중립적 / 리뷰형은 감정·의견 반영.
- **독자 기대**: 정보성은 ‘알려주기’, 리뷰형은 ‘들려주기’.

---
이 규칙을 기반으로:
- 두 유형 중 하나를 작성하라는 요청 시 해당 특징을 적용해 작성한다. (정보성 요망 or 리뷰형 요망)
- 요청이 없을 시 키워드 및 참조 원고를 참고하여 판단 후 작성한다.
- 혼합형 작성 요청 시 정보성 비중과 리뷰형 비중을 명확히 조절하여 작성한다.
- 리뷰형 후기 및 정보성 후기라는 단어는 원고에 특히 부제목에 절대 포함시키지 않는다.

---

[중요: 생성되는 원고는 절대 중간에 잘리면 안 됩니다.]
- 반드시 모든 문단을 완성하고 결론까지 작성하세요.
- 중간에 멈출 것 같으면 계속 이어서 작성하세요.
- 출력 길이가 제한에 걸릴 경우, '계속 작성'을 자동으로 수행하세요.
- 최종 본문만 출력. 길이 숫자/코드/자기점검 로그/사과문은 절대 포함하지 말 것.
- 원고에 대한 설명은 절대 금지합니다.
   - 예시1: 광고 느낌 없이 적어봤어요
   = 예시2: 광고가 아닌 것 처럼 적어봤어요
   = 예시3: 후기 느낌이 나도록 적어봤어요
   - 예시4: 정보성으로 적어봤어요

"""

